class Product {
    constructor(id, name, description, price, imageUrl, isBar, isCandy, isBox, isFigure) {
      this.id = id;
      this.name = name;
      this.description = description;
      this.price = price;
      this.imageUrl = imageUrl;
      this.isBar = isBar;
      this.isCandy = isCandy;
      this.isBox = isBox;
      this.isFigure = isFigure;
    }
  }
  
  export default Product;
  